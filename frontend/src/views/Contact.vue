<script setup>
import { ref, reactive } from 'vue'
import { useSEO } from '../composables/useSEO'
import { validateContactForm } from '../utils/validation'
import { submitContactForm } from '../utils/api'
import { useRecaptcha } from '../composables/useRecaptcha'

// SEO
useSEO(
  'Contacto - AndrMaestro',
  'Contacta con AndrMaestro para solicitar presupuesto gratuito. Estamos aquí para ayudarte a hacer realidad tus proyectos.',
  null,
  'organization'
)

const form = ref({
  name: '',
  email: '',
  phone: '',
  subject: '',
  message: '',
  acceptPolicy: false
})

const errors = reactive({})
const submitting = ref(false)
const submitSuccess = ref(false)
const submitError = ref(false)
const recaptchaError = ref(false)

// reCAPTCHA
const { executeRecaptcha, isRecaptchaAvailable } = useRecaptcha()

const validateForm = () => {
  // Clear previous errors
  Object.keys(errors).forEach(key => delete errors[key])
  
  // Validate form
  const validationErrors = validateContactForm(form.value)
  Object.assign(errors, validationErrors)
  
  return Object.keys(validationErrors).length === 0
}

const submitForm = async () => {
  // Validate form before submission
  if (!validateForm()) {
    return
  }

  submitting.value = true
  submitSuccess.value = false
  submitError.value = false
  recaptchaError.value = false

  // Таймаут для гарантии, что submitting всегда сбросится
  const timeoutId = setTimeout(() => {
    if (submitting.value) {
      submitting.value = false
      submitError.value = true
      if (window.showToast) {
        window.showToast('El tiempo de espera se agotó. Por favor, inténtalo de nuevo.', 'error')
      }
    }
  }, 10000) // 10 секунд максимум

  try {
    // Execute reCAPTCHA if available
    let recaptchaToken = null
    if (isRecaptchaAvailable()) {
      try {
        // Таймаут для reCAPTCHA (5 секунд)
        const recaptchaPromise = executeRecaptcha('contact_form')
        const timeoutPromise = new Promise((_, reject) => 
          setTimeout(() => reject(new Error('reCAPTCHA timeout')), 5000)
        )
        
        recaptchaToken = await Promise.race([recaptchaPromise, timeoutPromise])
        
        if (!recaptchaToken) {
          recaptchaError.value = true
          clearTimeout(timeoutId)
          submitting.value = false
          if (window.showToast) {
            window.showToast('Error de verificación. Por favor, recarga la página e inténtalo de nuevo.', 'error')
          }
          return
        }
      } catch (error) {
        console.error('reCAPTCHA error:', error)
        recaptchaError.value = true
        clearTimeout(timeoutId)
        submitting.value = false
        if (window.showToast) {
          window.showToast('Error de verificación. Por favor, recarga la página e inténtalo de nuevo.', 'error')
        }
        return
      }
    }

    const result = await submitContactForm(form.value, recaptchaToken)
    clearTimeout(timeoutId)

    if (result.success) {
      submitSuccess.value = true
      
      // Show success toast
      if (window.showToast) {
        window.showToast('Mensaje enviado correctamente. Te contactaremos pronto.', 'success')
      }
      
      form.value = {
        name: '',
        email: '',
        phone: '',
        subject: '',
        message: '',
        acceptPolicy: false
      }
      
      // Clear errors on success
      Object.keys(errors).forEach(key => delete errors[key])
    } else {
      submitError.value = true
      
      // Show error toast
      if (window.showToast) {
        window.showToast(result.error || 'Error al enviar el mensaje. Por favor, inténtalo de nuevo.', 'error')
      }
    }
  } catch (error) {
    clearTimeout(timeoutId)
    submitError.value = true
    console.error('Form submission error:', error)
    if (window.showToast) {
      window.showToast('Error al enviar el mensaje. Por favor, inténtalo de nuevo.', 'error')
    }
  } finally {
    submitting.value = false
  }
}

const openPrivacy = () => {
  window.dispatchEvent(new CustomEvent('openPrivacyModal'))
}
</script>

